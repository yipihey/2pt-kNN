/// 10. Dilution levels: per-level bands + lines + analytic
pub fn render_dilution_levels(data: &PlotData, config: &PlotConfig) -> String {
    let n = data.r_centers.len();
    let mut src = preamble(config);

    src.push_str(&format!("#let r-smooth = {}\n", fmt_array(&data.r_smooth)));
    src.push_str(&format!("#let xi-smooth = {}\n", fmt_array(&data.xi_smooth)));

    src.push_str(&format!(
        "\n#lq.diagram(\n  title: [Dilution Ladder: Per-Level $xi(r)$],\n  xlabel: [$r$ #h(0.3em) $[$h$\"\"^(-1)$ Mpc$]$],\n  ylabel: [$xi(r)$],\n  {},\n",
        diagram_opts(config),
    ));

    if let Some(ref tags) = data.level_tags {
        let max_level = *tags.iter().max().unwrap_or(&0);
        for level in 0..=max_level {
            let indices: Vec<usize> = (0..n).filter(|&i| tags[i] == level).collect();
            if indices.is_empty() { continue; }
            let r_l: Vec<f64> = indices.iter().map(|&i| data.r_centers[i]).collect();
            let lo: Vec<f64> = indices.iter().map(|&i| data.mean_xi[i] - data.std_xi[i]).collect();
            let hi: Vec<f64> = indices.iter().map(|&i| data.mean_xi[i] + data.std_xi[i]).collect();
            let xi_l: Vec<f64> = indices.iter().map(|&i| data.mean_xi[i]).collect();
            src.push_str(&format!(
                "  lq.fill-between({}, {}, {},\n    fill: level-colors.at({}).lighten(85%),\n    stroke: none),\n",
                fmt_array(&r_l), fmt_array(&lo), fmt_array(&hi), level,
            ));
            src.push_str(&format!(
                "  lq.plot({}, {},\n    stroke: (paint: level-colors.at({}), thickness: 2pt),\n    label: [Level {}]),\n",
                fmt_array(&r_l), fmt_array(&xi_l), level, level,
            ));
        }
    }

    src.push_str("  lq.plot(r-smooth, xi-smooth,\n    stroke: (dash: \"dashed\", paint: black, thickness: 1.5pt),\n    label: [Analytic]),\n");
    src.push_str(&format!(
        "  lq.vlines({:.8},\n    stroke: (dash: \"dotted\", paint: gray, thickness: 0.8pt)),\n",
        data.line_length,
    ));

    src.push_str(")\n");
    src
}

/// 11. Summary 2×2 figure: ξ, r²ξ, residuals, CDF
pub fn render_summary_figure(data: &PlotData, config: &PlotConfig) -> String {
    let n = data.r_centers.len();
    let mut src = preamble(config);

    // Data arrays shared across panels
    src.push_str(&format!("#let r = {}\n", fmt_array(&data.r_centers)));
    src.push_str(&format!("#let r-smooth = {}\n", fmt_array(&data.r_smooth)));
    src.push_str(&format!("#let xi-smooth = {}\n", fmt_array(&data.xi_smooth)));
    src.push_str(&format!("#let mean-xi = {}\n", fmt_array(&data.mean_xi)));
    src.push_str(&format!("#let bias = {}\n", fmt_array(&data.bias_sigma)));

    let lower: Vec<f64> = (0..n).map(|i| data.mean_xi[i] - data.std_xi[i]).collect();
    let upper: Vec<f64> = (0..n).map(|i| data.mean_xi[i] + data.std_xi[i]).collect();
    src.push_str(&format!("#let lower = {}\n", fmt_array(&lower)));
    src.push_str(&format!("#let upper = {}\n", fmt_array(&upper)));

    let r2xi_mean: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * data.mean_xi[i]).collect();
    let r2xi_lower: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * (data.mean_xi[i] - data.std_xi[i])).collect();
    let r2xi_upper: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * (data.mean_xi[i] + data.std_xi[i])).collect();
    let r2xi_analytic: Vec<f64> = data.r_smooth.iter().zip(data.xi_smooth.iter()).map(|(&r, &xi)| r * r * xi).collect();
    src.push_str(&format!("#let r2xi-mean = {}\n", fmt_array(&r2xi_mean)));
    src.push_str(&format!("#let r2xi-lower = {}\n", fmt_array(&r2xi_lower)));
    src.push_str(&format!("#let r2xi-upper = {}\n", fmt_array(&r2xi_upper)));
    src.push_str(&format!("#let r2xi-analytic = {}\n", fmt_array(&r2xi_analytic)));

    src.push_str("\n#show: lq.layout\n");
    src.push_str("#grid(columns: 2, column-gutter: 1.2em, row-gutter: 1em,\n");

    // Panel (a): ξ(r)
    src.push_str("  lq.diagram(\n    title: [(a) $xi(r)$],\n    xlabel: [$r$ #h(0.2em) $[$h$\"\"^(-1)$ Mpc$]$],\n    ylabel: [$xi(r)$],\n");
    src.push_str("    lq.fill-between(r, lower, upper,\n      fill: steelblue.lighten(80%),\n      stroke: none,\n      label: [kNN LS]),\n");
    src.push_str("    lq.plot(r, mean-xi,\n      stroke: (paint: steelblue, thickness: 2pt)),\n");
    src.push_str("    lq.plot(r-smooth, xi-smooth,\n      stroke: (dash: \"dashed\", paint: black, thickness: 1.5pt),\n      label: [Analytic]),\n");
    src.push_str("  ),\n");

    // Panel (b): r²ξ(r)
    src.push_str("  lq.diagram(\n    title: [(b) $r^2 xi(r)$],\n    xlabel: [$r$ #h(0.2em) $[$h$\"\"^(-1)$ Mpc$]$],\n    ylabel: [$r^2 xi(r)$],\n");
    src.push_str("    lq.fill-between(r, r2xi-lower, r2xi-upper,\n      fill: steelblue.lighten(80%),\n      stroke: none),\n");
    src.push_str("    lq.plot(r, r2xi-mean,\n      stroke: (paint: steelblue, thickness: 2pt)),\n");
    src.push_str("    lq.plot(r-smooth, r2xi-analytic,\n      stroke: (dash: \"dashed\", paint: black, thickness: 1.5pt)),\n");
    src.push_str("  ),\n");

    // Panel (c): Residuals
    src.push_str("  lq.diagram(\n    title: [(c) Bias / $sigma_\"mean\"$],\n    xlabel: [$r$ #h(0.2em) $[$h$\"\"^(-1)$ Mpc$]$],\n    ylabel: [$(hat(xi) - xi_\"true\") slash sigma$],\n");
    src.push_str("    lq.plot(r, bias,\n      stroke: none, mark: \"o\", mark-size: 3.5pt,\n      color: steelblue),\n");
    src.push_str("    lq.hlines(0,\n      stroke: (dash: \"dashed\", paint: black, thickness: 0.8pt)),\n");
    src.push_str("    lq.hlines(2,\n      stroke: (dash: \"dashed\", paint: crimson, thickness: 0.6pt)),\n");
    src.push_str("    lq.hlines(-2,\n      stroke: (dash: \"dashed\", paint: crimson, thickness: 0.6pt)),\n");
    src.push_str("  ),\n");

    // Panel (d): CDF
    src.push_str("  lq.diagram(\n    title: [(d) kNN-CDF vs Erlang (RR)],\n    xlabel: [$r$ #h(0.2em) $[$h$\"\"^(-1)$ Mpc$]$],\n    ylabel: [$\"CDF\"_k (r)$],\n");
    if let Some(ref cdf) = data.cdf_rr_summary {
        let colors_arr = ["steelblue", "crimson", "seagreen", "darkorange"];
        let r_arr = fmt_array(&cdf.r_values);
        for (idx, &k) in cdf.k_values.iter().enumerate() {
            let color = colors_arr[idx % colors_arr.len()];
            let measured = fmt_array(&cdf.cdf_mean[idx]);
            let erlang_vals: Vec<f64> = cdf.r_values.iter()
                .map(|&r| erlang_cdf(k, r, data.cdf_nbar))
                .collect();
            src.push_str(&format!(
                "    lq.plot({}, {},\n      stroke: (paint: {}, thickness: 1.5pt),\n      label: [$k = {}$]),\n",
                r_arr, measured, color, k,
            ));
            src.push_str(&format!(
                "    lq.plot({}, {},\n      stroke: (dash: \"dashed\", paint: black, thickness: 0.8pt)),\n",
                r_arr, fmt_array(&erlang_vals),
            ));
        }
    }
    src.push_str("  ),\n");

    src.push_str(")\n");
    src
}

/// Explorer 4×2 summary: 8 panels with independent view states.
pub fn render_explorer_summary(data: &PlotData, panel_configs: &[PlotConfig; 8]) -> String {
    // Use wider page for the 4×2 layout
    let mut src = format!(
        r##"#set page(width: 36cm, height: 26cm, margin: 5mm)
#set text(font: "New Computer Modern")
#import "@preview/lilaq:0.6.0" as lq
#let steelblue = rgb("#4682B4")
#let crimson = rgb("#DC143C")
#let seagreen = rgb("#2E8B57")
#let darkorange = rgb("#FF8C00")
#let level-colors = (steelblue, seagreen, darkorange, crimson)
"##
    );

    // Generate each panel's source inline within the grid
    let panel_fns: [fn(&PlotData, &PlotConfig) -> String; 8] = [
        render_xi_comparison,
        render_residuals,
        render_r2xi,
        render_cdf_comparison,
        render_peaked_cdf,
        render_individual_mocks,
        render_xi_ratio,
        render_timing,
    ];
    let labels = ["(a)", "(b)", "(c)", "(d)", "(e)", "(f)", "(g)", "(h)"];

    // We need to extract just the diagram portion from each render function.
    // Instead, build the grid directly with diagram calls.
    src.push_str("\n#show: lq.layout\n");
    src.push_str("#grid(columns: 4, column-gutter: 1em, row-gutter: 1em,\n");

    for (i, (panel_fn, label)) in panel_fns.iter().zip(labels.iter()).enumerate() {
        // Generate the full source for each panel, but we only need the diagram.
        // Extract the lq.diagram(...) call from the rendered source.
        let panel_src = panel_fn(data, &panel_configs[i]);
        // Find the diagram block — it starts after the preamble
        if let Some(diag_start) = panel_src.find("#lq.diagram(") {
            let diag = &panel_src[diag_start..];
            // Replace the title with the label prefix
            let labeled = diag.replacen("title:", &format!("// {}\n    title:", label), 1);
            src.push_str("  ");
            src.push_str(&labeled);
            // Remove trailing newline and add comma
            if src.ends_with('\n') {
                src.pop();
            }
            src.push_str(",\n");
        }
    }

    src.push_str(")\n");
    src
}

