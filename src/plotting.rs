/// 1. ξ(r) comparison: error band + mean line + analytic dashed + optional vline
pub fn render_xi_comparison(data: &PlotData, config: &PlotConfig) -> String {
    let n = data.r_centers.len();
    let mut src = preamble(config);

    // Data arrays
    src.push_str(&format!("#let r = {}\n", fmt_array(&data.r_centers)));
    src.push_str(&format!("#let r-smooth = {}\n", fmt_array(&data.r_smooth)));
    src.push_str(&format!("#let xi-smooth = {}\n", fmt_array(&data.xi_smooth)));
    src.push_str(&format!("#let mean-xi = {}\n", fmt_array(&data.mean_xi)));

    let lower: Vec<f64> = (0..n).map(|i| data.mean_xi[i] - data.std_xi[i]).collect();
    let upper: Vec<f64> = (0..n).map(|i| data.mean_xi[i] + data.std_xi[i]).collect();
    src.push_str(&format!("#let lower = {}\n", fmt_array(&lower)));
    src.push_str(&format!("#let upper = {}\n", fmt_array(&upper)));

    src.push_str(&format!(
        "\n#lq.diagram(\n  title: [CoxMock: $xi(r)$ Recovery],\n  xlabel: [$r$ #h(0.3em) $[$h$\"\"^(-1)$ Mpc$]$],\n  ylabel: [$xi(r)$],\n  {},\n",
        diagram_opts(config),
    ));

    // Per-level or single band
    if let Some(ref tags) = data.level_tags {
        let max_level = *tags.iter().max().unwrap_or(&0);
        for level in 0..=max_level {
            let indices: Vec<usize> = (0..n).filter(|&i| tags[i] == level).collect();
            if indices.is_empty() { continue; }
            let r_l: Vec<f64> = indices.iter().map(|&i| data.r_centers[i]).collect();
            let lo: Vec<f64> = indices.iter().map(|&i| data.mean_xi[i] - data.std_xi[i]).collect();
            let hi: Vec<f64> = indices.iter().map(|&i| data.mean_xi[i] + data.std_xi[i]).collect();
            let xi_l: Vec<f64> = indices.iter().map(|&i| data.mean_xi[i]).collect();
            src.push_str(&format!(
                "  lq.fill-between({}, {}, {},\n    fill: level-colors.at({}).lighten(80%),\n    stroke: none),\n",
                fmt_array(&r_l), fmt_array(&lo), fmt_array(&hi), level,
            ));
            src.push_str(&format!(
                "  lq.plot({}, {},\n    stroke: (paint: level-colors.at({}), thickness: 2pt),\n    label: [Level {}]),\n",
                fmt_array(&r_l), fmt_array(&xi_l), level, level,
            ));
        }
    } else {
        src.push_str("  lq.fill-between(r, lower, upper,\n    fill: steelblue.lighten(80%),\n    stroke: none,\n    label: [kNN LS $plus.minus 1 sigma$]),\n");
        src.push_str("  lq.plot(r, mean-xi,\n    stroke: (paint: steelblue, thickness: 2pt)),\n");
    }

    // Analytic
    src.push_str("  lq.plot(r-smooth, xi-smooth,\n    stroke: (dash: \"dashed\", paint: black, thickness: 1.5pt),\n    label: [Analytic $xi(r)$]),\n");

    // Reference line
    src.push_str(&format!(
        "  lq.vlines({:.8},\n    stroke: (dash: \"dotted\", paint: gray, thickness: 0.8pt)),\n",
        data.line_length,
    ));

    // Corrfunc overlay
    if let (Some(cf_mean), Some(cf_std)) = (&data.corrfunc_mean_xi, &data.corrfunc_std_xi) {
        src.push_str(&format!(
            "  lq.plot({}, {},\n    yerr: {},\n    stroke: none, mark: \"o\", mark-size: 4pt,\n    color: crimson,\n    label: [Corrfunc $plus.minus 1 sigma$]),\n",
            fmt_array(&data.r_centers), fmt_array(cf_mean), fmt_array(cf_std),
        ));
    }

    src.push_str(")\n");
    src
}

