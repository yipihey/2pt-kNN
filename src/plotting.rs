/// Format an f64 slice as a Typst array literal.
fn fmt_array(values: &[f64]) -> String {
    let items: Vec<String> = values.iter().map(|v| format!("{:.8}", v)).collect();
    format!("({})", items.join(", "))
}

/// Format scale parameter for lq.diagram.
fn fmt_scale(log: bool) -> &'static str {
    if log { "\"log\"" } else { "\"linear\"" }
}

/// Format axis limit for lq.diagram.
fn fmt_lim(range: Option<(f64, f64)>) -> String {
    match range {
        Some((lo, hi)) => format!("({:.8}, {:.8})", lo, hi),
        None => "auto".to_string(),
    }
}

/// Emit the standard typst preamble.
fn preamble(config: &PlotConfig) -> String {
    format!(
        r##"#set page(width: {w}cm, height: {h}cm, margin: 5mm)
#set text(font: "New Computer Modern")
#import "@preview/lilaq:0.6.0" as lq
#let steelblue = rgb("#4682B4")
#let crimson = rgb("#DC143C")
#let seagreen = rgb("#2E8B57")
#let darkorange = rgb("#FF8C00")
#let level-colors = (steelblue, seagreen, darkorange, crimson)
"##,
        w = config.width_cm,
        h = config.height_cm,
    )
}

/// Emit diagram options for scales and limits.
fn diagram_opts(config: &PlotConfig) -> String {
    format!(
        "xscale: {xs}, yscale: {ys}, xlim: {xl}, ylim: {yl}",
        xs = fmt_scale(config.log_x),
        ys = fmt_scale(config.log_y),
        xl = fmt_lim(config.x_range),
        yl = fmt_lim(config.y_range),
    )
}

