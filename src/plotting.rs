/// 3. r²ξ(r) comparison
pub fn render_r2xi(data: &PlotData, config: &PlotConfig) -> String {
    let n = data.r_centers.len();
    let mut src = preamble(config);

    src.push_str(&format!("#let r = {}\n", fmt_array(&data.r_centers)));

    let r2xi_analytic: Vec<f64> = data.r_smooth.iter()
        .zip(data.xi_smooth.iter())
        .map(|(&r, &xi)| r * r * xi)
        .collect();
    src.push_str(&format!("#let r-smooth = {}\n", fmt_array(&data.r_smooth)));
    src.push_str(&format!("#let r2xi-analytic = {}\n", fmt_array(&r2xi_analytic)));

    src.push_str(&format!(
        "\n#lq.diagram(\n  title: [$r^2 xi(r)$ Comparison],\n  xlabel: [$r$ #h(0.3em) $[$h$\"\"^(-1)$ Mpc$]$],\n  ylabel: [$r^2 xi(r)$ #h(0.3em) $[$h$\"\"^(-2)$ Mpc$\"\"^2]$],\n  {},\n",
        diagram_opts(config),
    ));

    if let Some(ref tags) = data.level_tags {
        let max_level = *tags.iter().max().unwrap_or(&0);
        for level in 0..=max_level {
            let indices: Vec<usize> = (0..n).filter(|&i| tags[i] == level).collect();
            if indices.is_empty() { continue; }
            let r_l: Vec<f64> = indices.iter().map(|&i| data.r_centers[i]).collect();
            let lo: Vec<f64> = indices.iter().map(|&i| data.r_centers[i].powi(2) * (data.mean_xi[i] - data.std_xi[i])).collect();
            let hi: Vec<f64> = indices.iter().map(|&i| data.r_centers[i].powi(2) * (data.mean_xi[i] + data.std_xi[i])).collect();
            let r2xi_l: Vec<f64> = indices.iter().map(|&i| data.r_centers[i].powi(2) * data.mean_xi[i]).collect();
            src.push_str(&format!(
                "  lq.fill-between({}, {}, {},\n    fill: level-colors.at({}).lighten(80%),\n    stroke: none),\n",
                fmt_array(&r_l), fmt_array(&lo), fmt_array(&hi), level,
            ));
            src.push_str(&format!(
                "  lq.plot({}, {},\n    stroke: (paint: level-colors.at({}), thickness: 2pt),\n    label: [Level {}]),\n",
                fmt_array(&r_l), fmt_array(&r2xi_l), level, level,
            ));
        }
    } else {
        let r2xi_mean: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * data.mean_xi[i]).collect();
        let r2xi_lower: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * (data.mean_xi[i] - data.std_xi[i])).collect();
        let r2xi_upper: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * (data.mean_xi[i] + data.std_xi[i])).collect();
        src.push_str(&format!("#let r2xi-mean = {}\n", fmt_array(&r2xi_mean)));
        src.push_str(&format!("#let r2xi-lower = {}\n", fmt_array(&r2xi_lower)));
        src.push_str(&format!("#let r2xi-upper = {}\n", fmt_array(&r2xi_upper)));
        src.push_str("  lq.fill-between(r, r2xi-lower, r2xi-upper,\n    fill: steelblue.lighten(80%),\n    stroke: none),\n");
        src.push_str("  lq.plot(r, r2xi-mean,\n    stroke: (paint: steelblue, thickness: 2pt),\n    label: [kNN LS]),\n");
    }

    src.push_str("  lq.plot(r-smooth, r2xi-analytic,\n    stroke: (dash: \"dashed\", paint: black, thickness: 1.5pt),\n    label: [Analytic]),\n");
    src.push_str(&format!(
        "  lq.vlines({:.8},\n    stroke: (dash: \"dotted\", paint: gray, thickness: 0.8pt)),\n",
        data.line_length,
    ));

    // Corrfunc overlay
    if let (Some(cf_mean), Some(cf_std)) = (&data.corrfunc_mean_xi, &data.corrfunc_std_xi) {
        let r2cf_mean: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * cf_mean[i]).collect();
        let r2cf_err: Vec<f64> = (0..n).map(|i| data.r_centers[i].powi(2) * cf_std[i]).collect();
        src.push_str(&format!(
            "  lq.plot({}, {},\n    yerr: {},\n    stroke: none, mark: \"o\", mark-size: 4pt,\n    color: crimson,\n    label: [Corrfunc]),\n",
            fmt_array(&data.r_centers), fmt_array(&r2cf_mean), fmt_array(&r2cf_err),
        ));
    }

    src.push_str(")\n");
    src
}

/// 4. CDF comparison: measured vs Erlang for each k
pub fn render_cdf_comparison(data: &PlotData, config: &PlotConfig) -> String {
    let mut src = preamble(config);
    let colors = ["steelblue", "crimson", "seagreen", "darkorange"];

    src.push_str(&format!(
        "\n#lq.diagram(\n  title: [kNN-CDF: Random Catalog vs Erlang],\n  xlabel: [$r$ #h(0.3em) $[$h$\"\"^(-1)$ Mpc$]$],\n  ylabel: [$\"CDF\"_k (r)$],\n  {},\n",
        diagram_opts(config),
    ));

    if let Some(ref cdf) = data.cdf_rr_summary {
        let r_arr = fmt_array(&cdf.r_values);
        for (idx, &k) in cdf.k_values.iter().enumerate() {
            let color = colors[idx % colors.len()];
            let measured = fmt_array(&cdf.cdf_mean[idx]);
            let erlang_vals: Vec<f64> = cdf.r_values.iter()
                .map(|&r| erlang_cdf(k, r, data.cdf_nbar))
                .collect();
            let erlang_arr = fmt_array(&erlang_vals);
            src.push_str(&format!(
                "  lq.plot({}, {},\n    stroke: (paint: {}, thickness: 2pt),\n    label: [$k = {}$ measured]),\n",
                r_arr, measured, color, k,
            ));
            src.push_str(&format!(
                "  lq.plot({}, {},\n    stroke: (dash: \"dashed\", paint: black, thickness: 1pt)),\n",
                r_arr, erlang_arr,
            ));
        }
    }

    src.push_str(")\n");
    src
}

/// 5. Peaked CDF (PDF): numerical dCDF/dr and analytic Erlang PDF
pub fn render_peaked_cdf(data: &PlotData, config: &PlotConfig) -> String {
    let mut src = preamble(config);
    let colors = ["steelblue", "crimson", "seagreen", "darkorange"];

    src.push_str(&format!(
        "\n#lq.diagram(\n  title: [kNN-PDF: $d$CDF$/d r$ (peaked CDF)],\n  xlabel: [$r$ #h(0.3em) $[$h$\"\"^(-1)$ Mpc$]$],\n  ylabel: [$d\"CDF\"_k slash d r$],\n  {},\n",
        diagram_opts(config),
    ));

    if let Some(ref cdf) = data.cdf_rr_summary {
        let r = &cdf.r_values;
        let nr = r.len();
        let r_arr = fmt_array(r);

        for (idx, &k) in cdf.k_values.iter().enumerate() {
            let color = colors[idx % colors.len()];
            let measured = &cdf.cdf_mean[idx];

            // Numerical derivative via central differences
            let mut pdf = vec![0.0; nr];
            if nr >= 3 {
                pdf[0] = (measured[1] - measured[0]) / (r[1] - r[0]);
                for i in 1..nr - 1 {
                    pdf[i] = (measured[i + 1] - measured[i - 1]) / (r[i + 1] - r[i - 1]);
                }
                pdf[nr - 1] = (measured[nr - 1] - measured[nr - 2]) / (r[nr - 1] - r[nr - 2]);
            }

            let analytic: Vec<f64> = r.iter().map(|&ri| erlang_pdf(k, ri, data.cdf_nbar)).collect();

            src.push_str(&format!(
                "  lq.plot({}, {},\n    stroke: (paint: {}, thickness: 2pt),\n    label: [$k = {}$ measured]),\n",
                r_arr, fmt_array(&pdf), color, k,
            ));
            src.push_str(&format!(
                "  lq.plot({}, {},\n    stroke: (dash: \"dashed\", paint: black, thickness: 1pt)),\n",
                r_arr, fmt_array(&analytic),
            ));
        }
    }

    src.push_str(")\n");
    src
}

