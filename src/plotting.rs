//! Typst + lilaq SVG plot generation for CoxMock validation results.
//!
//! Provides a unified plotting API used by validate.rs, the interactive
//! explorer, and the WASM web dashboard. All functions generate Typst
//! source strings using the lilaq plotting library, compiled in-memory
//! to SVG via `TypstPlotter`. No filesystem access — works on WASM.

use typst::foundations::Bytes;
use typst::layout::PagedDocument;
use typst::syntax::{FileId, Source, VirtualPath, package::{PackageSpec, PackageVersion}};
use typst_as_lib::TypstEngine;

use crate::diagnostics::{erlang_cdf, erlang_pdf};

// Vendored package sources generated by build.rs
include!(concat!(env!("OUT_DIR"), "/typst_packages.rs"));

// Embedded fonts
static FONT_REGULAR: &[u8] = include_bytes!("fonts/NewCM10-Regular.otf");
static FONT_MATH: &[u8] = include_bytes!("fonts/NewCMMath-Regular.otf");

// ---------------------------------------------------------------------------
// Infrastructure types
// ---------------------------------------------------------------------------

/// View configuration for a single plot.
#[derive(Clone, Debug)]
pub struct PlotConfig {
    pub width_cm: f64,
    pub height_cm: f64,
    pub log_x: bool,
    pub log_y: bool,
    pub x_range: Option<(f64, f64)>,
    pub y_range: Option<(f64, f64)>,
}

impl Default for PlotConfig {
    fn default() -> Self {
        Self {
            width_cm: 16.0,
            height_cm: 11.0,
            log_x: false,
            log_y: false,
            x_range: None,
            y_range: None,
        }
    }
}

/// All computed arrays needed for plotting, populated from validate.rs
/// after the mock loop. Shared data contract between validate.rs, explorer,
/// and WASM.
pub struct PlotData {
    pub r_centers: Vec<f64>,
    pub r_smooth: Vec<f64>,
    pub xi_smooth: Vec<f64>,
    pub mean_xi: Vec<f64>,
    pub std_xi: Vec<f64>,
    pub bias_sigma: Vec<f64>,
    pub all_xi: Vec<Vec<f64>>,
    pub line_length: f64,
    pub cdf_rr_summary: Option<crate::ladder::KnnCdfSummary>,
    pub cdf_nbar: f64,
    // Corrfunc reference data (all Optional so explorer works without it)
    pub corrfunc_mean_xi: Option<Vec<f64>>,
    pub corrfunc_std_xi: Option<Vec<f64>>,
    pub corrfunc_all_xi: Option<Vec<Vec<f64>>>,
    pub knn_times: Option<Vec<f64>>,
    pub corrfunc_times: Option<Vec<f64>>,
    pub level_tags: Option<Vec<usize>>,
}

/// Caches vendored typst sources for fast repeated compilation.
pub struct TypstPlotter {
    sources: Vec<Source>,
    binaries: Vec<(FileId, Bytes)>,
}

impl TypstPlotter {
    /// Parse vendored sources/binaries once for reuse across renders.
    pub fn new() -> Self {
        Self {
            sources: vendored_sources(),
            binaries: vendored_binaries(),
        }
    }

    /// Compile a Typst source string to SVG.
    pub fn render(&self, typst_source: &str) -> String {
        let engine = TypstEngine::builder()
            .main_file(typst_source)
            .fonts([FONT_REGULAR as &[u8], FONT_MATH as &[u8]])
            .with_static_source_file_resolver(self.sources.clone())
            .with_static_file_resolver(self.binaries.clone())
            .build();

        let result = engine.compile::<PagedDocument>();
        match result.output {
            Ok(doc) => {
                if let Some(page) = doc.pages.first() {
                    typst_svg::svg(page)
                } else {
                    String::from("<svg/>")
                }
            }
            Err(e) => {
                let warnings: Vec<String> =
                    result.warnings.iter().map(|w| format!("{:?}", w)).collect();
                panic!("Typst compilation failed: {}\nWarnings: {:?}", e, warnings);
            }
        }
    }
}

// ---------------------------------------------------------------------------
// Typst internals — vendored source loading
// ---------------------------------------------------------------------------

fn make_spec(namespace: &str, name: &str, version: &str) -> PackageSpec {
    let ver = parse_version(version);
    PackageSpec {
        namespace: namespace.into(),
        name: name.into(),
        version: ver,
    }
}

fn vendored_sources() -> Vec<Source> {
    package_sources()
        .into_iter()
        .map(|(namespace, name, version, path, content)| {
            let spec = make_spec(namespace, name, version);
            let file_id = FileId::new(Some(spec), VirtualPath::new(path));
            Source::new(file_id, content.to_string())
        })
        .collect()
}

fn vendored_binaries() -> Vec<(FileId, Bytes)> {
    package_binaries()
        .into_iter()
        .map(|(namespace, name, version, path, content)| {
            let spec = make_spec(namespace, name, version);
            let file_id = FileId::new(Some(spec), VirtualPath::new(path));
            (file_id, Bytes::new(content.to_vec()))
        })
        .collect()
}

fn parse_version(s: &str) -> PackageVersion {
    let parts: Vec<u32> = s.split('.').map(|p| p.parse().unwrap_or(0)).collect();
    PackageVersion {
        major: parts.first().copied().unwrap_or(0),
        minor: parts.get(1).copied().unwrap_or(0),
        patch: parts.get(2).copied().unwrap_or(0),
    }
}

